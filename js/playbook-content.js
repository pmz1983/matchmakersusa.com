function checkAccess() {
  var code = document.getElementById('accessCode').value.trim().toUpperCase();
  var err = document.getElementById('gateError');
  if (!code) { err.textContent = 'Please enter your access code.'; return; }

  // First: check promo codes from checkout system
  if (typeof lookupPromo === 'function') {
    var promo = lookupPromo(code);
    if (promo && promo.type === 'free') {
      unlockPlaybook();
      return;
    }
  }

  // Second: check if it's a MM- access code (generated by webhook)
  if (code.startsWith('MM-') && code.length >= 11) {
    // Verify against Supabase
    err.textContent = 'Verifying...';
    err.style.color = 'var(--sl,#7A95AF)';
    verifyAccessCode(code, err);
    return;
  }

  // Not recognized
  err.textContent = 'Code not recognized. Please check your code and try again.';
  err.style.color = '';
  document.getElementById('accessCode').style.borderColor = 'rgba(229,115,115,.5)';
}

function verifyAccessCode(code, errEl) {
  // For now, accept any MM- prefixed code locally (webhook-generated codes)
  // In production, this would verify against Supabase purchases table
  unlockPlaybook();
}

// Email-based access verification
function checkAccessByEmail() {
  var emailInput = document.getElementById('gateEmail');
  var err = document.getElementById('gateError');
  if (!emailInput) return;
  var email = emailInput.value.trim().toLowerCase();
  if (!email || email.indexOf('@') === -1) {
    err.textContent = 'Please enter a valid email address.';
    err.style.color = '';
    return;
  }

  err.textContent = 'Checking your purchase...';
  err.style.color = 'var(--sl,#7A95AF)';

  fetch(SUPABASE_FN_URL + '/check-eligibility', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ email: email, product: 'playbook', action: 'verify-access' })
  })
    .then(function (r) { return r.json(); })
    .then(function (data) {
      if (data.has_access) {
        unlockPlaybook();
      } else {
        err.textContent = 'No purchase found for this email. Check the email you used at checkout.';
        err.style.color = '';
      }
    })
    .catch(function () {
      err.textContent = 'Connection error. Please try again.';
      err.style.color = '';
    });
}

function unlockPlaybook() {
  document.getElementById('pb-gate').style.display = 'none';
  document.body.style.overflow = '';
  localStorage.setItem('pb_access', '1');
}

// Bind gate events
var accessCodeInput = document.getElementById('accessCode');
if (accessCodeInput) {
  accessCodeInput.addEventListener('keydown', function (e) {
    if (e.key === 'Enter') checkAccess();
  });
}
var gateEmailInput = document.getElementById('gateEmail');
if (gateEmailInput) {
  gateEmailInput.addEventListener('keydown', function (e) {
    if (e.key === 'Enter') checkAccessByEmail();
  });
}

// Auto-unlock if already verified
if (localStorage.getItem('pb_access') === '1') {
  var gate = document.getElementById('pb-gate');
  if (gate) gate.style.display = 'none';
}

