var SUPABASE_FN_URL = 'https://peamviowxkyaglyjpagc.supabase.co/functions/v1';

// ── Progress bar ──
window.addEventListener('scroll', function () {
  var h = document.documentElement;
  var f = document.getElementById('pbProgress');
  if (f) f.style.width = (window.scrollY / (h.scrollHeight - h.clientHeight) * 100) + '%';
}, { passive: true });

// ── Active nav ──
var navLinks = document.querySelectorAll('.pb-nav a[href^="#"]');
var chapterEls = document.querySelectorAll('[id^="ch"]');
if (window.IntersectionObserver && chapterEls.length) {
  var observer = new IntersectionObserver(function (entries) {
    entries.forEach(function (e) {
      if (e.isIntersecting) {
        navLinks.forEach(function (a) { a.classList.remove('active'); });
        var l = document.querySelector('.pb-nav a[href="#' + e.target.id + '"]');
        if (l) l.classList.add('active');
      }
    });
  }, { rootMargin: '-15% 0px -75% 0px' });
  chapterEls.forEach(function (c) { observer.observe(c); });
}

// ── Smooth scroll ──
function scrollTo(id) {
  var el = document.getElementById(id);
  if (el) window.scrollTo({ top: el.offsetTop - 80, behavior: 'smooth' });
  return false;
}
document.querySelectorAll('a[href^="#"]').forEach(function (a) {
  a.addEventListener('click', function (e) {
    e.preventDefault();
    var id = a.getAttribute('href').slice(1);
    scrollTo(id);
  });
});

// ── Copy button ──
function doCopy(btn) {
  var q = btn.closest('.sc-body').querySelector('.sc-quote');
  if (q) navigator.clipboard.writeText(q.textContent.replace(/\u201C/g, '').replace(/\u201D/g, '').trim())
    .then(function () {
      btn.textContent = 'COPIED';
      btn.classList.add('copied');
      setTimeout(function () { btn.textContent = 'COPY'; btn.classList.remove('copied'); }, 2000);
    });
}

